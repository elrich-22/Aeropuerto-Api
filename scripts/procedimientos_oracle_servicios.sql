    P_VUE_ID_VUELO IN AER_VUELO.VUE_ID_VUELO%TYPE
)
AS
BEGIN
    DELETE FROM AER_VUELO
     WHERE VUE_ID_VUELO = P_VUE_ID_VUELO;

    IF SQL%ROWCOUNT = 0 THEN
        DBMS_OUTPUT.PUT_LINE('No se encontro registro para eliminar en AER_VUELO.');
    ELSE
        DBMS_OUTPUT.PUT_LINE('Registro eliminado de AER_VUELO.');
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error al eliminar en AER_VUELO: ' || SQLERRM);
        RAISE;
END;
