CREATE OR REPLACE PROCEDURE SP_AER_VUELO_OBTENER_POR_ID (
    P_VUE_ID_VUELO IN AER_VUELO.VUE_ID_VUELO%TYPE,
    P_RESULTADO OUT SYS_REFCURSOR
)
AS
BEGIN
