SELECT
                    AER_ID,
                    AER_CODIGO_AEROPUERTO,
                    AER_NOMBRE,
                    AER_CIUDAD,
                    AER_PAIS,
                    AER_ZONA_HORARIA,
                    AER_ESTADO,
                    AER_TIPO,
                    AER_LATITUD,
                    AER_LONGITUD,
                    AER_CODIGO_IATA,
                    AER_CODIGO_ICAO,
                    AER_FECHA_REGISTRO
                FROM AER_AEROPUERTO
                ORDER BY AER_ID
